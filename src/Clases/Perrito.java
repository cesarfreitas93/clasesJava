package Clases;

public class Perrito {
    float edad;
    String nombre;
    String raza;
    Boolean estadoSlud;


    public float getEdad() {
        return edad;
    }

    public void setEdad(float edad) {
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public Boolean getEstadoSlud() {
        return estadoSlud;
    }

    public void setEstadoSlud(Boolean estadoSlud) {
        this.estadoSlud = estadoSlud;
    }
}
